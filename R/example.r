#' An example addition function
#' @param x1 is the first number in the addition
#' @param x2 is the second number in the addition
#' @export 
add2 <- function(x1, x2) {
  x1 + x2
}

#' An example multiplication function
#' @param x1 is the first number in the multiplication
#' @param x2 is the second number in the multiplication
#' @export 
multiply2 <- function(x1, x2) {
  x1 * x2
}
